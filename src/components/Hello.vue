<template>
  <div class="hello1">
    <h1>{{ msg }}</h1>
    <h2>Essential Links</h2>
     <button v-on:click="gr">Greet</button>
     <my-component></my-component>
  </div>
</template>

<script>
export default {
  name: 'hello',
  data () {
    return {
      msg: 'Welcome to Your Vue.js App',
      name: 'name的值'
    }
  },
  methods: {
    gr: function (event) {
      // `this` 在方法里指当前 Vue 实例
      alert('Hello ' + this.name + '!')
      // `event` 是原生 DOM 事件
      if (event) {
        alert(event.target.tagName)
      }
    }
  }
}
</script>

